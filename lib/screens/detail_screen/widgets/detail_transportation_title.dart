class DetailTransportationTitle extends StatelessWidget {
  final TransportModel transport;

  const DetailTransportationTitle({Key? key, required this.transport})
      : super(key: key);

  String localizationToTransport(
      {required String key, required BuildContext context}) {
    switch (key) {
      case "directions_train":
        return AppLocalizations.of(context)!.directions_train;
      case "directions_bus":
        return AppLocalizations.of(context)!.directions_bus;
      case "directions_car":
        return AppLocalizations.of(context)!.directions_car;
      case "directions_walk":
        return AppLocalizations.of(context)!.directions_walk;
    }
    return '';
  }

